    private void cargarCliente() {
        try {
            Cliente cliente = getDatosCliente();
            cliente = dbService.buscarCliente(cliente);
            if (cliente != null) {
                DefaultTableModel model = (DefaultTableModel) clientTable.getModel();
                model.addRow(new Object[]{cliente.getClienteId(), cliente.getNombres(), cliente.getApellidos(), cliente.getCedula(), cliente.getRif(), cliente.getTelefono()});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de acceso a base de datos", JOptionPane.ERROR_MESSAGE);
        }     
    }

    private Cliente getDatosCliente() throws Exception {
        String cedula = cedulaField.getText();
        if (!cedula.trim().isEmpty())
            cedula = null;
        String rif = rifField.getText();
        if (!rif.trim().isEmpty())
            rif = null;
        return new Cliente(cedula, rif);
    }
